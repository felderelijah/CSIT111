# values are already given not user interactive
speed = 60
time1 = 5
time2 = 8
time3 = 12

# known formula for distance
distance = speed * time1
distance2 = speed * time2
distance3 = speed * time3

# output based on given values
print('The distance the car will travel in 5 hours is', distance)
print('The distance the car will travel in 8 hours is', distance2)
print('The distance the car will travel in 12 hours is', distance3)

